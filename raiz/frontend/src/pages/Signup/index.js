// src/pages/Signup/index.js
import React, { useState } from "react";
import { Link, useNavigate } from "react-router-dom";
import { useAuth } from "../../hooks/useAuth";
import { useNotification } from '../../contexts/NotificationContext';
import Input from "../../components/Input";
import Button from "../../components/Button";
import {
  Container,
  FormBox,
  LogoIcon,
  Title,
  ErrorMessage,
  FooterText,
  InputGroup,
  PasswordRules,
  ValidationItem,
  AnimatedSpan,
  SuccessPopupOverlay,
  SuccessPopupContent,
  SuccessPopupLogo,
  SuccessPopupTitle
} from "./styles";

const Signup = () => {
  const { signup } = useAuth();
  const navigate = useNavigate();
  const { notify } = useNotification();
  const [nome, setNome] = useState("");
  const [email, setEmail] = useState("");
  const [emailConf, setEmailConf] = useState("");
  const [senha, setSenha] = useState("");
  const [senhaConf, setSenhaConf] = useState("");
  const [error, setError] = useState("");
  const [showSuccessPopup, setShowSuccessPopup] = useState(false);
  
  // Valida√ß√µes de senha
  const [passwordValidations, setPasswordValidations] = useState({
    length: false,
    uppercase: false,
    lowercase: false,
    number: false,
    specialChar: false,
  });

  // Estados para controle de campos tocados
  const [fieldTouched, setFieldTouched] = useState({
    nome: false,
    email: false,
    emailConf: false,
    senha: false,
    senhaConf: false
  });

  // Fun√ß√£o para validar caracteres do nome
  const validateNameCharacters = (name) => {
    // Permite letras, espa√ßos, h√≠fens e caracteres acentuados
    return /^[a-zA-Z√Ä-√ø\s-]+$/.test(name);
  };

  // Formata√ß√£o do nome
  const formatName = (name) => {
    // Remove caracteres inv√°lidos
    let formatted = name.replace(/[^a-zA-Z√Ä-√ø\s-]/g, '');
    // Remove m√∫ltiplos espa√ßos
    formatted = formatted.replace(/\s{2,}/g, ' ');
    // Formata capitaliza√ß√£o
    return formatted.split(' ')
      .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
      .join(' ');
  };

  // Handlers de mudan√ßa
  const handleNomeChange = (e) => {
    const rawValue = e.target.value;
    
    // Verifica caracteres inv√°lidos
    if (!validateNameCharacters(rawValue) && rawValue !== "") {
      notify("‚ö†Ô∏è O nome deve conter apenas letras, espa√ßos e h√≠fens", "warning");
      return;
    }
    
    setNome(formatName(rawValue));
    setError("");
  };

  const handleEmailChange = (e) => {
    setEmail(e.target.value.toLowerCase());
    setError("");
  };

  const handleEmailConfChange = (e) => {
    setEmailConf(e.target.value.toLowerCase());
    setError("");
  };

  const handleSenhaChange = (e) => {
    const newPassword = e.target.value;
    setSenha(newPassword);
    validatePassword(newPassword);
    setError("");
  };

  const handleSenhaConfChange = (e) => {
    setSenhaConf(e.target.value);
    setError("");
  };

  // Handler para quando o campo perde o foco
  const handleBlur = (fieldName) => {
    setFieldTouched({ ...fieldTouched, [fieldName]: true });
    validateField(fieldName);
  };

  // Valida√ß√µes
  const validatePassword = (password) => {
    const validations = {
      length: password.length >= 8,
      uppercase: /[A-Z]/.test(password),
      lowercase: /[a-z]/.test(password),
      number: /\d/.test(password),
      specialChar: /[!@#$%^&*(),.?":{}|<>]/.test(password),
    };
    setPasswordValidations(validations);
    return Object.values(validations).every(v => v);
  };

  const validateNome = (nome) => {
    const trimmed = nome.trim();
    // Verifica quantidade de palavras, tamanho e caracteres v√°lidos
    return trimmed.split(" ").length >= 2 && 
           trimmed.length >= 5 && 
           validateNameCharacters(trimmed);
  };

  const validateEmail = (email) => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);

  const validateEmailConfirmation = (email, emailConf) => email === emailConf;

  const validatePasswordConfirmation = (senha, senhaConf) => senha === senhaConf;

  // Valida√ß√£o individual de campo
  const validateField = (fieldName) => {
    if (!fieldTouched[fieldName]) return;

    switch (fieldName) {
      case 'nome':
        if (!nome.trim()) {
          notify("üë§ O campo Nome Completo √© obrigat√≥rio!", "error");
        } else if (!validateNome(nome)) {
          notify("üë§ Digite nome completo v√°lido (m√≠nimo 2 palavras, 5 caracteres, apenas letras, espa√ßos e h√≠fens)!", "error");
        }
        break;
      case 'email':
        if (!email.trim()) {
          notify("üìß O campo E-mail √© obrigat√≥rio!", "error");
        } else if (!validateEmail(email)) {
          notify("üìß E-mail inv√°lido! Use o formato exemplo@dominio.com", "error");
        }
        break;
      case 'emailConf':
        if (!emailConf.trim()) {
          notify("üìß O campo Confirma√ß√£o de E-mail √© obrigat√≥rio!", "error");
        } else if (!validateEmailConfirmation(email, emailConf)) {
          notify("üìß Os e-mails n√£o coincidem!", "error");
        }
        break;
      case 'senha':
        if (!senha) {
          notify("üîì O campo Senha √© obrigat√≥rio!", "error");
        } else if (!Object.values(passwordValidations).every(v => v)) {
          notify("üîì A senha n√£o atende a todos os requisitos de seguran√ßa!", "error");
        }
        break;
      case 'senhaConf':
        if (!senhaConf) {
          notify("üîí O campo Confirma√ß√£o de Senha √© obrigat√≥rio!", "error");
        } else if (!validatePasswordConfirmation(senha, senhaConf)) {
          notify("üîí As senhas n√£o coincidem!", "error");
        }
        break;
      default:
        break;
    }
  };

  // Fun√ß√£o para coletar todos os erros
  const getValidationErrors = () => {
    const errors = [];

    if (!nome.trim()) {
      errors.push("üë§ O campo Nome Completo √© obrigat√≥rio!");
    } else if (!validateNome(nome)) {
      errors.push("üë§ Nome inv√°lido! Deve conter:\n- M√≠nimo 2 palavras\n- M√≠nimo 5 caracteres\n- Apenas letras, espa√ßos e h√≠fens");
    }

    if (!email.trim()) {
      errors.push("üìß O campo E-mail √© obrigat√≥rio!");
    } else if (!validateEmail(email)) {
      errors.push("üìß E-mail inv√°lido! Use o formato exemplo@dominio.com");
    }

    if (!emailConf.trim()) {
      errors.push("üìß O campo Confirma√ß√£o de E-mail √© obrigat√≥rio!");
    } else if (!validateEmailConfirmation(email, emailConf)) {
      errors.push("üìß Os e-mails n√£o coincidem!");
    }

    if (!senha) {
      errors.push("üîì O campo Senha √© obrigat√≥rio!");
    } else if (!Object.values(passwordValidations).every(v => v)) {
      errors.push("üîì A senha n√£o atende a todos os requisitos de seguran√ßa!");
    }

    if (!senhaConf) {
      errors.push("üîí O campo Confirma√ß√£o de Senha √© obrigat√≥rio!");
    } else if (!validatePasswordConfirmation(senha, senhaConf)) {
      errors.push("üîí As senhas n√£o coincidem!");
    }

    return errors;
  };

  const handleSignup = async () => {
    const validationErrors = getValidationErrors();
    
    if (validationErrors.length > 0) {
      validationErrors.forEach(error => notify(error, 'error'));
      return;
    }
  
    try {
      const errorMessage = await signup(nome, email, senha);
      
      if (errorMessage) {
        notify(errorMessage, 'error');
        console.error('Detalhes do erro:', {
          message: errorMessage,
        });
      } else {
        notify('üéâ Cadastro realizado com sucesso!', 'success');
        setShowSuccessPopup(true);
      }
      
    } catch (error) {
      console.error('Erro completo:', {
        message: error.message,
        response: error.response?.data,
        status: error.response?.status,
        stack: error.stack
      });
      
      notify('‚ùå Erro ao conectar com o servidor', 'error');
    }
  };

  return (
    <Container>
      <FormBox>
        <LogoIcon>
          <img src="/assets/images/logo.png" alt="Logo" />
        </LogoIcon>
        <Title>Crie <AnimatedSpan>sua conta</AnimatedSpan></Title>
        {error && <ErrorMessage>{error}</ErrorMessage>}

        <InputGroup>
          <Input
            type="text"
            placeholder="Nome Completo"
            emoji="üë§"
            value={nome}
            onChange={handleNomeChange}
            onBlur={() => handleBlur('nome')}
          />
        </InputGroup>

        <InputGroup>
          <Input
            type="email"
            placeholder="E-mail"
            emoji="‚úâ"
            value={email}
            onChange={handleEmailChange}
            onBlur={() => handleBlur('email')}
          />
        </InputGroup>

        <InputGroup>
          <Input
            type="email"
            placeholder="Confirme o E-mail"
            emoji="üìß"
            value={emailConf}
            onChange={handleEmailConfChange}
            onBlur={() => handleBlur('emailConf')}
          />
        </InputGroup>

        <InputGroup>
          <Input
            type="password"
            placeholder="Crie sua Senha"
            emoji="üîì"
            value={senha}
            onChange={handleSenhaChange}
            onBlur={() => handleBlur('senha')}
          />
        </InputGroup>

        <PasswordRules>
          <ValidationItem $valid={passwordValidations.length}>
            ‚úì Ter mais de 8 caracteres
          </ValidationItem>
          <ValidationItem $valid={passwordValidations.uppercase}>
            ‚úì Letra mai√∫scula
          </ValidationItem>
          <ValidationItem $valid={passwordValidations.lowercase}>
            ‚úì Letra min√∫scula
          </ValidationItem>
          <ValidationItem $valid={passwordValidations.number}>
            ‚úì N√∫mero
          </ValidationItem>
          <ValidationItem $valid={passwordValidations.specialChar}>
            ‚úì Caractere especial(! , @ , # , $ , * ...)
          </ValidationItem>
        </PasswordRules>

        <InputGroup>
          <Input
            type="password"
            placeholder="Confirme a Senha"
            emoji="üîí"
            value={senhaConf}
            onChange={handleSenhaConfChange}
            onBlur={() => handleBlur('senhaConf')}
          />
        </InputGroup>

        <Button onClick={handleSignup}>üöÄ Cadastrar Agora</Button>

        <FooterText>
          J√° tem conta? <Link to="/signin">Fa√ßa Login ‚û°Ô∏è</Link>
        </FooterText>
      </FormBox>

      {showSuccessPopup && (
        <SuccessPopupOverlay>
          <SuccessPopupContent>
            <SuccessPopupLogo>
              <img src="/assets/images/logo.png" alt="Logo" />
            </SuccessPopupLogo>
            <SuccessPopupTitle>Cadastro Conclu√≠do!</SuccessPopupTitle>
            <p style={{ marginBottom: '20px' }}>Sua conta foi criada com sucesso!</p>
            <Button 
              onClick={() => {
                setShowSuccessPopup(false);
                navigate('/signin');
              }}
              style={{ width: '100%' }}
            >
              Fazer LoginüöÄ
            </Button>
          </SuccessPopupContent>
        </SuccessPopupOverlay>
      )}
    </Container>
  );
};

export default Signup;